public class Animal {
    private int eyes;
    private int legs;
    private int teeth;
    private int tail;
    private String coat;

    public Animal(int eyes, int legs, int teeth, int tail, String coat) {
        this.eyes = eyes;
        this.legs = legs;
        this.teeth = teeth;
        this.tail = tail;
        this.coat = coat;
    }

}
